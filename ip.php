                    <div class="col-xl-12">


                        <p>Protecting your Intellectual Property (IP) is not compulsory but it is worth consideration. Just as having a contracted guard or a burglar alarm on your home is not required by law, it is a choice many homeowners make for peace of mind. As well as protecting your IP against infringement, protecting your IP can add financial value to your company, particularly if you are selling at a later stage. Protected Intellectual Property rights can be used to facilitate licensing revenue, as security for borrowing and for marketing purposes.
                            The protection of your Intellectual Property will also give you exclusive rights (in countries where you have secured protection) to your particular asset e.g. brand name, invention, design etc. This also ensures that others cannot benefit financially from your hard work or, indeed, damage your reputation by forbidding others from creating an inferior product of service that appears to be associated with your business.
                        </p>
                        <p>Nowadays, a company’s or individual’s intangible assets are often more valuable than physical assets. Intangible assets can include human capital, know-how, inventions, brands, designs and other products of intellectual creativity.
                            Intellectual property can be a vital ingredient in securing the commercial success of any company or individual wishing to stay ahead of the field in creating innovative new products, expanding market share and generating customer loyalty. It is very much in the interests of business to make best use of intangible assets by investigating the opportunities offered by the intellectual property system.

                            Intellectual Property Rights are territorial. Therefore, if you seek Kenyan protection, you will be granted protection in the Republic of Kenya only. However, further protection can be sought from relevant individual countries or, alternatively, there are International routes available also
                        </p>
                    </div>

                    <div class="col-xl-12">
                        <div class=" my-2">

                            <p class="about-one__business-start-text">Which Kind of IP is Right for You</p>
                            <p>Regardless of what your business or service is, it is highly likely that one or more of the main Intellectual Property rights will be applicable.</p>
                            <p><b>In order to determine which IP rights are best for you, consider the following:</b></p>
                            <p>
                                Almost every business will have a trade name, brand, sign or product name that can be protected with a Trade Mark.
                            </p>
                            <p>Have you created a new product or technical process? Or technically modified an existing product? If so, you may be eligible file an application for Patent protection.
                                Are you a jewellery, furniture or fashion designer? Perhaps you are making pottery or ornaments? Or have you simply modified or changed the APPEARANCE of an existing product… if so, then perhaps Design Protection will be applicable.
                                If you have created website content, produced advertising material or training manuals, written a song or a book or created a work of art, you will have automatic Copyright Protection.
                                Or maybe you have a food product with a unique process or a unique technical process which may fall under Trade Secrets.
                                It is not uncommon for more than one right to be applicable.
                            </p>
                        </div>

                    </div>
                    <div class="col-xl-12">
                        <div class=" my-2">

                            <p class="about-one__business-start-text">Enforcing Your IP</p>
                            <p>Getting IP protection means that you can ensure that the investment of resources in creating the IP can lead to economic reward and further innovation. An infringement of IP rights occurs when others unlawfully use the IPR belonging to another organization or individual. Examples of this include the unauthorized manufacturing of patented technology; the sale of counterfeit products; similar goods bearing another company's trade mark; distribution of music or film without the copyright owner's consent (pirated goods).
                                Obtaining IP protection can give you a competitive advantage allowing you to sell those products or services at a premium. However, having IP rights in place is meaningless unless those rights are enforced. The threat of enforcement is what allows the IP to be exploited as a commercial asset. Competitors or other third parties may still infringe on your intellectual property rights. If that happens, there are steps you can take to end the infringement.
                            </p>
                            <ol>
                                <li>
                                    <p>Enforcement of IP rights is necessary in order to:</p>
                                    <ul>
                                        <li> Preserve the legal validity of your IP rights.</li>
                                        <li>Prevent infringement from occurring or continuing in the marketplace in order to avoid damage including loss of goodwill or reputation.</li>
                                        <li>Seek compensation for actual damage, e.g. loss of profit, resulting from the infringement.
                                            IP Rights owners can take court action in response to alleged infringing activity. The courts have a wide range of remedies available to them to compensate aggrieved owners. These include:
                                            <ul>
                                                <li>Damages</li>
                                                <li>Injunctions</li>
                                                <li> Orders to account for profits</li>
                                                <li>Orders to seize, destroy and/or hand over infringing goods to right holders. </li>
                                            </ul>
                                        </li>
                                    </ul>
                                </li>

                            </ol>
                            <p>If you are in a position where you need take steps to enforce your Intellectual Property rights, there are a number of ways, depending on the circumstances of infringement, in which you can proceed.
                                Should an IP right infringement occur it is suggested that alternative mechanisms to resolve disputes are explored first and that, only when this is not a viable solution, you enforce your rights through legal proceedings by filling a complaint with relevant government agencies such as the Anti-counterfeit authority(ACA), Kenya Copyright Board(KECOBO), Kenya Industrial Property Institute (KIPI)
                            </p>
                            <p>An initial step, particularly in the case of Trade Mark infringement, can be the issuing of a cease and desist letter drawn up by a solicitor. Due to the threat of court action if the infringement continues, in a lot of cases, this action might be sufficient to deter the infringer.
                                It is always advisable to seek legal assistance to determine how best to proceed and always seek professional legal advice from a solicitor or IP professional before embarking on enforcement proceedings.
                            </p>
                            <p>Enforcement of intellectual property (IP) rights in Kenya is complex and requires careful consideration. Enforcement actions will require the legal expertise of IP professionals with relevant experience to advise and provide representation.
                                For more information please consult the IPRASK Helpdesk on defending and enforcing IP rights in Kenya.
                            </p>

