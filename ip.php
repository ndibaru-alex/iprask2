                                <h2 class="section-title__title">What is Intelectual Property?</h2>
                            </div>
                            <p>The type of property that is familiar to most people comprises physical objects. People own clothes, cars, houses and land. Intellectual Property (IP), on the other hand, is the product of someone's mental efforts. Thus an individual's creativity and innovation can be owned in much the same way that they can own physical property. National and International laws and conventions recognize the product of a person's mental efforts as an Intellectual Property right (IPR)</p>
                            <p>IP is all around us. Every product or service that we use in our daily lives is usually the result of a long chain of big or small innovations, such as changes in designs, or improvements that make a product look or function the way it does today. Regardless of what product an enterprise makes or what services it provides, it is likely that it already has or is regularly using and creating a great deal of IP. For example, almost every business enterprise in Kenya has a trade name and may have one or more brands and should consider protecting them under the Kenyan law as trademarks. Many will develop creative original designs. Many will have produced, or assisted in the publication, dissemination or retailing of a copyrighted work. Some may have invented or improved a product or service. </p>
                            <p>Some forms of IP require formal application and examination before you can claim a right to ownership. Others, such as copyright, do not.
                                So applying for an IP right to protect your idea can be critical if you want to build a business and establish your presence in the Kenyan market. It is also important to understand the different types of IP and the advantages of each.
                            </p>

                            <ol class="counter-one__list ">
                                <h4>TYPES OF IP</h4>
                                <p>The main intellectual property rights cover a number of areas, including Patents for Inventions, Trade Marks, Industrial Designs and Copyright.</p>
                                <li><b>Patents</b> are concerned with inventions producing a technical result - of new and improved products, processes and uses that are capable of industrial application. </li>

                                <li><b>Trade Marks </b> are concerned with brand identity - principally of goods and services. They can be distinctive words, signs or other features, the purpose of which is to make a distinction in the mind of a customer between different traders, products and services. An individual, a company, a partnership, a society, a co-operative or any lawful association can apply for a trade mark provided they meet the requirements of the Trade Marks Act.
                                    In order for a trade mark (other than a certification mark) to be registrable in Kenya, under the Trade Marks Act (Cap 506) it must contain or consist of at least one of the following essential particulars:-
                                    <ul>
                                        <li> The name of a company , individual or firm , represented in a special or particular manner</li>
                                        <li>The signature of the applicant for registration or some predecessor in his business</li>
                                        <li> An invented word or invented words;</li>
                                        <li> A word or words having no direct reference to the character or quality of the goods, and not being according to its ordinary signification a geographical name or a surname.</li>
                                        <li> Any other distinctive mark.</li>
                                    </ul>
                                </li>
                                <li><b>Designs </b>
                                    <p>are concerned with the appearance of a product - of the whole or a part of a product - features of, in particular, shape, configuration, contours, texture or materials of the product itself not dictated by functional considerations. </p>
                                    <p>According to industrial property Act 2001, an industrial design is defined as "any composition of lines or colors or any three dimensional form whether or not associated with lines or colors, provided that such composition or form gives a special appearance to a product of industry or handicraft and can serve as pattern for a product of industry or handicraft".
                                        In other words an industrial design is concerned only with the outward appearance (eye appeal) of articles as defined by their shape, configuration, pattern or ornament. It must be understood that an industrial design DOES NOT protect the method of construction or the function of the article.
                                    </p>
                                    <ul>
                                        <p>An application for registration of an industrial design should contain</p>
                                        <li> A completed application form (form IP 27)</li>
                                        <li> Two identical specimens of the design</li>
                                        <li>Drawings, photographs or other graphic representations of the article embodying the industrial design and an indication of the kind of products for which the industrial design is to be used</li>
                                        <li> The prescribed application fee.</li>
                                        <li>Where the applicant is not the creator, the request must be accompanied by a statement justifying the applicant's right to the registration of the industrial design</li>
                                        <li>A power of attorney, where the applicant is represented by an agent;</li>


                                    </ul>
                                </li>
                                <li><b>Copyright</b>
                                    <p>is concerned with original literary works such as novels, poems and plays, musical and artistic works such as musical compositions, sound recordings and TV and radio broadcasts, software, multimedia films, drawings, maps, charts, plans, paintings, photographs and sculptures and works of architecture. Rights related to copyright include those of performing artists in their performances, producers of phonograms in their recordings, and those of broadcasters in their radio and television programs.
                                        It should also be noted that intellectual property also encompasses such areas as know- how, trade secrets, protection of plant varieties etc. It is often the case that more than one type of intellectual property right can be applied to the same creation
                                    </p>
                                </li>
                            </ol>

